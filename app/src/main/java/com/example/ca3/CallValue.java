package com.example.ca3;

public class CallValue {
    int image;
    String name, team;

    public CallValue(int image, String name, String team) {
        this.image = image;
        this.name = name;
        this.team = team;
    }
    public int getImage() {
        return image;
    }
    public String getName() {
        return name;
    }
    public String getTeam() {
        return team;
    }
}
